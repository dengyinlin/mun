<h1 class="page-header"> About Us </h1>
<p></p>
<p style="text-align:justify;line-height:200%; font-size:125%">Ever since the time the Mahajanapad of Kashi, the cultural capital of the world's biggest
democracy has been witness to diplomacy of varied backgrounds at the zenith of
governance. 2k13 would be seeing the return of such times. With the celebration of the life
and times of one of the pioneers of democratic development and our visionary founder,
Mahamana Pt. Madan Mohan Malviya, Indian Institute of Technology (BHU) Varanasi
proudly presents the First Edition of IIT-BHU MUN to celebrate his 150th birth anniversary.</p>

<p style="text-align:justify;line-height:200%;  font-size:125%">Model United Nations (also Model UN or MUN) is an academic simulation of the United
Nations that aims to educate participants about current events, topics in international
relations, diplomacy and the United Nations agenda. The participants role-play as diplomats
representing a nation or NGO in a simulated session of a committee of the United Nations.
Participants research a country, take on roles as diplomats, investigate international issues,
debate, deliberate, consult, and then develop solutions to world problems. More recently,
simulation of other deliberative bodies, such as the United Nations Security Council, has
been included in Model United Nations, even if they are completely unrelated to the UN or
international affairs as a whole.</p>

<p style="text-align:justify;line-height:200%; font-size:125%">Being a confluence of not just great thinkers, an MUN would definitely witness the outcome
of a whole new dimension of thought process and development of ideas to tackle global
challenges. Being held in conjunction with Kashiyatra'13, this is the ideal platform to bring
out the diplomat in you. Come join us, witness this spectacle and enjoy all that this city has
to offer!</p>
